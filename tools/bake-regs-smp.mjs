// bake-regs-smp.mjs — turn "info registers -a" (all CPUs) into src/snapregs-smp.HC: a SetSnapRegs()
// that seeds EVERY core's CCpuState slot (g_cpu_st[k]) plus the shared system state. Run worker 0
// calls this once; all workers then see the per-core state in shared memory.
//   node tools/bake-regs-smp.mjs [/tmp/hemusnap/regs_smp.txt]
import { readFileSync, writeFileSync } from "node:fs";
const path = process.argv[2] || "/tmp/hemusnap/regs_smp.txt";
const raw = readFileSync(path, "latin1");
const blocks = raw.split(/CPU#\d+/).slice(1);            // one block per CPU
const N = blocks.length;
if (!N) throw new Error("no CPU blocks in " + path);
const hx = (t, re) => { const m = t.match(re); if (!m) throw new Error("missing " + re + " in a CPU block"); return "0x" + m[1].toLowerCase().replace(/^0+(?=.)/, ""); };
const segBase = (t, s) => "0x" + (t.match(new RegExp(`${s}\\s*=[0-9a-f]+\\s+([0-9a-f]+)`, "i"))[1].toLowerCase().replace(/^0+(?=.)/, "") || "0");
const segSel  = (t, s) => "0x" + (t.match(new RegExp(`${s}\\s*=([0-9a-f]+)`, "i"))[1].toLowerCase().replace(/^0+(?=.)/, "") || "0");
const RN = ["RAX","RCX","RDX","RBX","RSP","RBP","RSI","RDI","R8","R9","R10","R11","R12","R13","R14","R15"];
const rrx = (t, n) => hx(t, new RegExp(`${n}\\s*=([0-9a-f]+)`, "i"));    // R8 has a space before =
let body = "";
for (let k = 0; k < N; k++) {
  const t = blocks[k];
  const regs = RN.map((n) => rrx(t, n));
  const rip = hx(t, /RIP=([0-9a-f]+)/i), rfl = hx(t, /RFL=([0-9a-f]+)/i);
  const fsb = segBase(t, "FS"), gsb = segBase(t, "GS");
  const hlt = /HLT=1/.test(t) ? 1 : 0;
  body += `  // --- core ${k} ---\n`;
  for (let i = 0; i < 16; i++) body += `  g_cpu_st[${k}].f_reg[${i}]=${regs[i]};\n`;
  body += `  g_cpu_st[${k}].f_rip=${rip}; g_cpu_st[${k}].f_rfl=${rfl}; g_cpu_st[${k}].f_halted=${hlt};\n`;
  body += `  g_cpu_st[${k}].f_msr_fsbase=${fsb}; g_cpu_st[${k}].f_msr_gsbase=${gsb};\n`;
}
// shared system state from CPU#0
const t0 = blocks[0];
const CR0 = hx(t0, /CR0=([0-9a-f]+)/i), CR3 = hx(t0, /CR3=([0-9a-f]+)/i), CR4 = hx(t0, /CR4=([0-9a-f]+)/i), EFER = hx(t0, /EFER=([0-9a-f]+)/i);
const CS = segSel(t0, "CS"), SS = segSel(t0, "SS");
const gdt = t0.match(/GDT=\s+([0-9a-f]+)\s+([0-9a-f]+)/i), idt = t0.match(/IDT=\s+([0-9a-f]+)\s+([0-9a-f]+)/i);
const norm = (h) => "0x" + h.toLowerCase().replace(/^0+(?=.)/, "");
const out = `//============================================================================
// snapregs-smp.HC — GENERATED by tools/bake-regs-smp.mjs from ${path} (${N} cores).
// Seeds every core's CCpuState (g_cpu_st[k]) + shared system state for SMP resume.
//============================================================================
U0 SetSnapRegs()
{
  g_ncore=${N};
${body}  // shared system state
  cr0=${CR0}; cr3=${CR3}; cr4=${CR4}; msr_efer=${EFER};
  gdtr_base=${norm(gdt[1])}; gdtr_limit=${norm(gdt[2])}; idtr_base=${norm(idt[1])}; idtr_limit=${norm(idt[2])};
  g_cs=${CS}; g_ss=${SS}; icount=0; g_badop=0;
  pic1_base=0x20; pic2_base=0x28;
  pic1_mask=0xF8; pic2_mask=0xEF; pic1_irr=0; pic2_irr=0; pic1_isr=0; pic2_isr=0;
  pit_div=20000; pit_counter=20000; tsc=0; irq_count=0; hpet_ctr=0;
  rtc_idx=0; rtc_base_sec=5819; rtc_dow=3; rtc_day=3; rtc_mon=6; rtc_year=24;
}
`;
writeFileSync(new URL("../src/snapregs-smp.HC", import.meta.url), out);
console.log(`wrote src/snapregs-smp.HC: ${N} cores; core0 rip=${hx(blocks[0],/RIP=([0-9a-f]+)/i)} hlt=${/HLT=1/.test(blocks[0])?1:0}`);
for (let k = 0; k < N; k++) console.log(`  core ${k}: rip=${hx(blocks[k],/RIP=([0-9a-f]+)/i)} rsp=${rrx(blocks[k],"RSP")} fs=${segBase(blocks[k],"FS")} hlt=${/HLT=1/.test(blocks[k])?1:0}`);
